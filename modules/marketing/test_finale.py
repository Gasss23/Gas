# Campagna Marketing - Idee Iniziali
#
# 1. Campagna "Scopri la Novità":
#    Focus su un nuovo prodotto/funzionalità con sconti per i primi acquirenti e influencer marketing.
#    Canali: Social Media (Instagram, TikTok), Email Marketing, Blog.
#
# 2. Campagna "Ritorno al Futuro":
#    Promozione di un prodotto classico o evergreen con un tocco moderno, evidenziando la sua rilevanza attuale.
#    Canali: Facebook Ads, Google Search Ads, Collaborazioni con blog di settore.
#
# 3. Campagna "Feedback & Vinci":
#    Incentivare i clienti a lasciare recensioni e feedback in cambio di premi o partecipazione a un concorso.
#    Canali: Email post-acquisto, Sezione recensioni sul sito, Social Media.
